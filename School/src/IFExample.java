
public class IFExample {

	public static void main(String[] args) {
		
		int num1 = 5, num2 = 10;

		if (num1 > num2)
			System.out.println(num1 + " is greater than " + num2);
		else if (num1 == num2)
			System.out.println("Same");
		else
			System.out.println(num2 + " is greater than " + num1);
		
	}

}
